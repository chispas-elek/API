package org.api.packwhatsaudit;

public class Auditoria {

	//ATRIBUTOS
	private int id;
	private String nombre;

	//MÉTODO CONSTRUCTOR	
	public Auditoria(int id, String nombre) {
		super();
		this.id = id;
		this.nombre = nombre;
	}

	//GETTERS Y SETTERS	
	public int getId() {
		return id;
	}

	public String getNombre() {
		return nombre;
	}

	//MÉTODOS DEFINIDOS	

}
